package com.loka.autotesting.logger.exceptions;

public class NpeAssertionError extends AssertionError {
	public NpeAssertionError() {
	}

	public NpeAssertionError(Object detailMessage) {
		super(detailMessage);
	}

	public NpeAssertionError(boolean detailMessage) {
		super(detailMessage);
	}

	public NpeAssertionError(char detailMessage) {
		super(detailMessage);
	}

	public NpeAssertionError(int detailMessage) {
		super(detailMessage);
	}

	public NpeAssertionError(long detailMessage) {
		super(detailMessage);
	}

	public NpeAssertionError(float detailMessage) {
		super(detailMessage);
	}

	public NpeAssertionError(double detailMessage) {
		super(detailMessage);
	}

	public NpeAssertionError(String message, Throwable cause) {
		super(message, cause);
	}
}
